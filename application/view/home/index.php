                           <p><strong>Dada</strong><br>
                           Ceux qui me connaissent bien ne s'étonneront pas de ma passion pour ce type d'art aussi très francais.<br>
                           Le mouvement dada, ou dadaïsme, est un mouvement intellectuel, littéraire et artistique du début du xxe siècle, qui se caractérise par une remise en cause de toutes<br>
                           les conventions et contraintes idéologiques, esthétiques et politiques.<br>
                           « Nous ne sommes pas assez naïfs pour croire dans le progrès. Nous ne nous occupons, avec amusement, que de l’aujourd’hui.<br>
                           Nous voulons être des mystiques du détail, des taraudeurs et des clairvoyants, des anti-conceptionnistes et des râleurs littéraires.<br>
                           Nous voulons supprimer le désir pour toute forme de beauté, de culture, de poésie, pour tout raffinement intellectuel, toute forme de goût, socialisme, altruisme et synonymisme. »<br>
                              Tout est dit.</p>

                           <p><strong>Surréalisme.</strong><br>
                           Le surréalisme est un mouvement artistique du xxe siècle, comprenant l’ensemble des procédés de création et d’expression utilisant toutes les forces psychiques (automatisme, rêve, inconscient)<br>
                           libérées du contrôle de la raison et en lutte contre les valeurs reçues.<br>
                           André Breton le définit dans le premier Manifeste du surréalisme comme un « automatisme psychique pur, par lequel on se propose d'exprimer, soit verbalement, soit par écrit, soit de toute autre manière,<br>
                           le fonctionnement réel de la pensée. Dictée de la pensée, en l'absence de tout contrôle exercé par la raison, en dehors de toute préoccupation esthétique ou morale »<br>
                               Mouvement porté par des visionnaires comme Dali ou Apollinaire.</p>

