                           <p><strong>Impressionisme</strong><br>
                           Ce mouvement pictural typiquement francais fortement critiqué à ses débuts a marqué une réelle rupture avec les formes d'art de l'époque.<br>
                           Il était porté par des artistes comme Monet, Renoir, Bazille, Cézanne et j'en passe.<br>
