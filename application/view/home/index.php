                            <p><strong>Hordes du Contrevent d'Alain Damasio</strong><br>
                            Bouquin de science fiction d'un auteur que j'admire énormément.<br>
                            Raconte une expédition d'une très jeune équipe qui souhaite explorer une partie du monde inconnue et affronter le vent.<br>
                            Ce phénomène météorologique va les freiner constamment dans leur progression et se présentera sous des formes très différentes.<br>
                            L'équipe doit alors s'adapter et prendre en considération les avis des anciens pour tenter de faire mieux.<br>
                                Un vrai roman poétique et visionnaire.</p>

                            <p><strong>Tous les matins du monde de Paul Quignard</strong><br>
                            Un livre centré sur la musique.<br>
                            A la perte de sa femme, le personnage principal va se focaliser sur la viole, son instrument de prédilection.<br>
                            Il se perfectionnera si bien qu'il arrivera à imiter toutes les inflexions de la voix humaine pour se rappeler son amour perdu.<br>
                                On y apprend que l'art dans toutes ses formes est une belle catharsis permettant d'extérioriser ses douleurs les plus profondes.</p>

                            <p><strong>Le Loup des steppes de Hermann Hesse</strong><br>
                            J'aime énormément cet auteur allemand connu pour ses romans qui racontent de beaux parcours initiatiques.<br>
                            Il raconte l’histoire de Harry Haller, homme désabusé, tiraillé entre un besoin d’isolement, presque de sauvagerie, un aspect de lui-même qu’il nomme « le loup des steppes »,<br>
                            Et l’intégration dans la société, qu’il recherche malgré tout, encore et toujours.<br>
                            La découverte d'un fascicule décrivant sa propre histoire, ainsi qu'une belle rencontre vont l'obliger à sortir de son existence recluse et à se confronter aux multiples aspects de sa personnalité.<br>
                            Il entame ainsi un parcours initiatique qui le fera passer par toutes les facettes possibles de son existence.<br>
                                Il apprend ainsi à jouir de la vie et à utiliser l'humour pour se distancier de l'absurdité du monde et progresser.</p>

                            <p><strong>Sur la route de Jack Kerouac</strong><br>
                            Un roman racontant le phénomèhne de la "Beat Generation" aux Etats Unis.<br>
                            C'est une ode aux grands espaces, à l'épopée vers l'ouest, à la découverte des mondes nouveaux.<br>
                            Alliant créativité débordante et fascination pour les milieux underground des villes des côtes Est et Ouest des États-Unis et tout l'art qui s'y crée (littérature, jazz, etc.),<br>
                            la Beat Generation témoigne également d'un attachement profond aux grands espaces, à la nature et aux spiritualités chamaniques dans lesquelles l'homme est partie intégrante du Cosmos.<br>
                            Des grands intellectuels rejettent en effet le modèle actuel de la société, et se cherchent.<br>
                            Ils vivent alors de manière décousue comme des vagabonds de villes en villes dans des squats.<br>
                                Ils se cherchent.</p>

                            <p><strong>vieux qui ne voulait pas fêter son anniversaire de Jonas Jonasson</strong><br>
                            Un petit best seller très drole et léger.<br>
                            Il raconte le destin absurde d'un homme qui a atteint ses 100 ans. Ce destin fait beaucoup penser à Forrest Gump dans son genre.<br>
                            Suivre sa boussole intérieure et ne privilégier des choix liés à ses tripes et non à sa raison peut apporter énormément.<br>
                            Humainement.<br>
                                Mais pas seulement.</p>

                            <p><strong>La nuit des temps de Barjavel</strong><br>
                            Ce classique de science fiction raconte l'histoire d'une humanité qui a reculé.<br>
                            Elle a fait par le passé des progrès scientifiques phénoménaux, mais l'avidité de pouvoir des humains et leurs guerres ont eu raison de ses progrès.<br>
                                Mais une belle lecon en est tiré. Car si les hommes s'unissent maintenant dans un même rêve, alors tous ensemble ils peuvent réaliser de très belles choses.</p>

                            <p><strong>Voyage au centre de la terre de Jules Verne</strong><br>
                            Un des tout premiers romans d'un auteur qui a bercé mon enfance.<br>
                            Une belle histoire d'aventure liée à des prouesses techniques hors norme.<br>
                            Je vous avoue que je m'en souviens peu car j'ai lu ces bouquins il y a longtemps mais j'en garde de très beaux souvenirs rêveurs.<br>

                            <p><strong>Poésies de Rimbaud</strong><br>
                            Une personnalité que j'admire par sa vie décousue et ses engagements.<br>
                            Je comprends pas tout ce qu'il écrit, mais ca a l'air très beau.<br>
                            Ou l'on découvre que la poésie a un énorme pouvoir sur l'humain.<br>
